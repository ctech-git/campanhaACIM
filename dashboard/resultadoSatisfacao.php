    const element = data[i];
    var dataNascimento = element.dataNascimento;
    var nome = element.nome;
    var telefone = element.telefone;

    var mes = dataNascimento.split('/');
    var dia = mes[0];
    var ano = mes[2];
    mes = mes[1];

